function add_N(a: number, b: number) {
    return a + b;
}

function min_N(a: number, b: number) {
    return a - b;
}

function mul_N(a: number, b: number) {
    return a * b;
}

function div_N(a: number, b: number) {
    return a / b;
}

console.log(add_N(3, 4));
console.log(min_N(3, 4));
console.log(mul_N(3, 4));
console.log(div_N(3, 4));